//INBUILT JS ARRAY METHOD
var arr = [12 , 14 , 56 , 57]

console.log(arr)

//POP() METHOD - REMOVE AN ELEMENT FROM END OF THE ARRAY
var c = arr.pop()
console.log(`Poped element `+c)

//PUSH() METHOD - ADD AN ELEMENT TO THE END OF THE ARRAY

var c = arr.push(100)
console.log(`Pushed element `+c)

// SHIFT() METHOD - REMOVE FIRST ELEMENT FROM THE ARRAY 

var c = arr.shift()
console.log(`Shifted element `+c)
console.log(arr)

// UNSHIFT() METHOD - ADD AN ELEMENT AT THE STARTING OF THE ARRAY

var c = arr.unshift(20)
console.log(`Unshifted element `+c)
console.log(arr)
